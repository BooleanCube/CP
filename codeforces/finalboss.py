getint = lambda : int(input())
getlist = lambda : list(map(int, input().split()))
getstr = lambda : list(input()) # mutable string

def solve():
    h, n = getlist()
    a, c = getlist(), getlist()
    pq = PriorityQueue()
    for i in range(n): pq.put((1, a[i], c[i]))
    ans = 0
    while 1:
        cur, dmg, cool = pq.get()
        h -= dmg
        if h <= 0:
            ans = cur
            break
        pq.put((cur+cool, dmg, cool))
    print(ans)

testcases = 1
testcases = getint()
